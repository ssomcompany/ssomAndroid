package com.ssomcompany.ssomclient.purchase.model;

public class StoreProduct {
    private String id;
    private String name;
    private String kind;
    private double price;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getKind() {
        return kind;
    }

    public double getPrice() {
        return price;
    }
}
